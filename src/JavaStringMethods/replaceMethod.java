package JavaStringMethods;

public class replaceMethod {
    public static void main(String[] args) {
        /*
        The replace() method searches a string for a specified character,
        and returns a new string where the specified character(s) are replaced.
         */
        String myStr = "Hello";
        System.out.println(myStr.replace('l', 'p'));// Heppo
        //Returns:	A new String, where the specified character has been replaced by the new character(s)
    }
}
